<h3>Performance Rating Definitions</h3>

(1) Unacceptable: A Performance Improvement Plan (PIP) will be initiated for any individual performance rating of Unacceptable. 
<ul class="instructions">
	<li>Performance does not meet minimum acceptable standards, expectations, and requirements of the job, or is below what can be expected of average performance</li>
	<li>Employee requires a high level of supervision or assistance to accomplish work results</li>
	<li>Improvement is necessary to meet desired level of performance</li>
</ul>
(2)	Acceptable
<ul class="instructions">
	<li>Performance meets acceptable standards, expectations, and requirements</li>
	<li>Performance contributes what is expected of a qualified, experienced employee performing in this position</li>
	<li>Employee is expected to continue development of new knowledge, skills, or abilities</li>
</ul>
(3)	Commendable
<ul class="instructions">
	<li>Consistently meets standards and expectations, regularly exceeds them, and shows initiative in additional assignments</li>
	<li>Successfully completes all responsibilities, even for projects that require versatile skills</li>
	<li>Employee strives to grow professionally through development activities</li>
</ul>
(4)	Exemplary
<ul class="instructions">
	<li>Performance is noticeably exceptional; outstanding performance is clearly evident; performance is at a level that is a “stretch”</li>
	<li>Performance shows exceptional initiative to plan and anticipate problems, and employee takes appropriate independent action</li>
	<li>Performance requires little or no supervision to produce exceptional results</li>
</ul>
(N/A) Not Applicable
<ul class="instructions">
	<li>This rating can be used when the category does not apply to the employee’s job duties or it may be too soon to rate the employee’s performance in this category</li>
</ul>
